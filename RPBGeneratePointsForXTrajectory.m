 % RPBGeneratePointsForXTrajectory generate a trajectory tpi, dar given
 % parameters
 
% Author: Renata Porciuncula Baptista
% E-mail: renata.porciunculabaptista@cea.fr


function [kx, ky, kz] = RPBGeneratePointsForXTrajectory( trajectory, theta, phi,...
                                    p, spatialResolution,...
                                    nPointsKspaceTrajectory, ...
                                    gamma, dt, grad_raster_time)   
                                

    if strcmp(trajectory, 'tpi') 
    [kx, ky, kz] = RPBGeneratePointsForTPITrajectory(theta, phi,...
                                        p, spatialResolution,...
                                        nPointsKspaceTrajectory, ...
                                        gamma, dt, grad_raster_time);
    elseif strcmp(trajectory, 'dar')
    [kx, ky, kz] = RPBGeneratePointsForDARTrajectory(theta, phi,...
                                        p, spatialResolution,...
                                        nPointsKspaceTrajectory, ...
                                        gamma, dt, grad_raster_time);   
    else
        disp('Trajectory not implemented yet')
    end
end

